package com.cdut.sign.util;

public class ChildInfor {
	
	private int signImage;
	private int signOutImage;
	private String signDay; 
	private String signOutDay; 
	private String signTime; 
	private String signOutTime; 
	   
  
    public ChildInfor() 
    {
    }   
  
    public int getSignImage() 
    {   
        return signImage;   
    }   
  
    public void setsignImage(int signImage) 
    {   
        this.signImage = signImage;   
    }
    
    public int getSignOutImage() 
    {   
        return signOutImage;   
    }   
  
    public void setsignOutImage(int signOutImage) 
    {   
        this.signOutImage = signOutImage;   
    }
  
    public String getSignDay() {   
        return signDay;   
    }   
  
    public void setSignDay(String signDay) {   
        this.signDay = signDay;   
    }   
  
    public String getSignOutDay() {   
        return signOutDay;   
    }   
  
    public void setSignOutDay(String signOutDay) {   
        this.signOutDay = signOutDay;   
    } 
    
    public String getSignTime() {   
        return signTime;   
    }   
  
    public void setSignTime(String signTime) {   
        this.signTime = signTime;   
    }   
  
    public String getSignOutTime() {   
        return signOutTime;   
    }   
  
    public void setSignOutTime(String signOutTime) {   
        this.signOutTime = signOutTime;   
    }
}
