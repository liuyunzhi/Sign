package com.cdut.sign.util;

public class RecordItem {

    private String groupDate;

    public RecordItem() {
    }

    public RecordItem(String groupDate) {
        this.groupDate = groupDate;
    }

    public void setGroupDate(String groupDate) {
        this.groupDate = groupDate;
    }

    public String getGroupDate() {
        return groupDate;
    }
}
